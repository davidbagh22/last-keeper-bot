def empty_parameters() -> dict[str, int]:
    return {key: 0 for key in PARAMETERS}


def final_archetype(values: dict[str, int]) -> tuple[str, str, str]:
    p = empty_parameters()
    p.update(values)

    balance_keys = ('memory', 'unity', 'cultural_code', 'responsibility')
    balance_values = [p[key] for key in balance_keys]
    balanced = min(balance_values) >= 2 and max(balance_values) - min(balance_values) <= 3

    if balanced and p['truth'] >= 1 and p['living_language'] >= 0:
        return (
            'Общее наследие',
            'Ваш Архив не замер в прошлом и не растворился в переменах. Он хранит факты, голоса и культурные связи, оставляя будущим Хранителям не готовый ответ, а ответственное наследие.',
            'common_heritage',
        )
    if p['living_language'] + p['unity'] >= 6 and p['truth'] <= 2:
        return (
            'Живой архив',
            'Вы сделали память понятной и близкой людям. Архив заговорил живым языком, но часть точности теперь зависит от честности тех, кто продолжит рассказ.',
            'living_archive',
        )
    if p['truth'] + p['memory'] >= 8 and p['living_language'] + p['unity'] <= 1:
        return (
            'Закрытый архив',
            'Вы сохранили документы и точные формы. Архив уцелел почти без потерь, но войти в него смогут только те, кто уже знает его язык.',
            'closed_archive',
        )
    if p['responsibility'] + p['truth'] >= 7 and min(p['unity'], p['living_language']) <= 0:
        return (
            'Героическая память',
            'Вы защитили доказанное и значимое, но обычные человеческие голоса стали тише. Архив помнит подвиг — и просит вернуть лица тем, кто остался за рамкой.',
            'heroic_memory',
        )
    if p['progress'] >= 4 and p['unity'] + p['cultural_code'] <= 2:
        return (
            'Холодный прогресс',
            'Вы открыли Архив будущему. Он стал быстрым и технологичным, но в нём осталось меньше человеческого тепла и культурных опор.',
            'cold_progress',
        )
    return (
        'Общее наследие',
        'Ваш Архив сохранил разные силы: точность, живой язык, память и ответственность. Их равновесие ещё не завершено — значит, история остаётся открытой для следующего Хранителя.',
        'common_heritage',
    )


def format_event_date(value: str) -> str:
    try:
        return datetime.fromisoformat(value).strftime('%d.%m.%Y')
    except ValueError:
        return value


def route_lines(team: str) -> list[str]:
    lines: list[str] = []
    for index, key in enumerate(ROUTES[team]):
        if key == 'open':
            title, place = OPEN_SPACES['title'], OPEN_SPACES['place']
        else:
            title, place = LOCATIONS[key]['title'], LOCATIONS[key]['place']
        lines.append(f'{index + 1}. {TIME_SLOTS[index]} — {title}\n   {place}')
    return lines
